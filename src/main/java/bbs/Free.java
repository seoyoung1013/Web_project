package bbs;

public class Free {
	
	private int free_ID;
	private String free_Title;
	private String free_userID;
	private String free_Date;
	private String free_Content;
	private int free_Available;
	private int free_count;
	private int free_like_count;
	private int free_category;

	
	public int getFree_ID() {
		return free_ID;
	}
	public void setFree_ID(int free_ID) {
		this.free_ID = free_ID;
	}
	public String getFree_Title() {
		return free_Title;
	}
	public void setFree_Title(String free_Title) {
		this.free_Title = free_Title;
	}
	public String getFree_userID() {
		return free_userID;
	}
	public void setFree_userID(String free_userID) {
		this.free_userID = free_userID;
	}
	public String getFree_Date() {
		return free_Date;
	}
	public void setFree_Date(String free_Date) {
		this.free_Date = free_Date;
	}
	public String getFree_Content() {
		return free_Content;
	}
	public void setFree_Content(String free_Content) {
		this.free_Content = free_Content;
	}
	public int getFree_Available() {
		return free_Available;
	}
	public void setFree_Available(int free_Available) {
		this.free_Available = free_Available;
	}
	public int getFree_count() {
		return free_count;
	}
	public void setFree_count(int free_count) {
		this.free_count = free_count;
	}
	public int getFree_like_count() {
		return free_like_count;
	}
	public void setFree_like_count(int free_like_count) {
		this.free_like_count = free_like_count;
	}
	public int getFree_category() {
		return free_category;
	}
	public void setFree_category(int free_category) {
		this.free_category = free_category;
	}
	
}
